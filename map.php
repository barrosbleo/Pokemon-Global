function checkKeysUp(evt) {
	if (evt.keyCode == 38) {
		moveSprite('up');
	} else if (evt.keyCode == 37) {
		moveSprite('left');
	} else if (evt.keyCode == 39) {
		moveSprite('right');
	} else if (evt.keyCode == 40) {
		moveSprite('down');
	}
	//alert(evt.keyCode);
}

function checkKeysDown(evt){
	if (evt.keyCode >= 37 || evt.keyCode <= 40 ) {
		if(evt.preventDefault){
			evt.preventDefault();
		}
		if (evt.stopPropagation) {
			evt.stopPropagation();
		}
	}
}

